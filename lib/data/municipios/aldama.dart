## Economía

**Pesca**
Principal actividad económica. Se captura camarón, jaiba, huachinango, mojarra, ostión, y pulpo. Hay 15 cooperativas pesqueras en La Pesca, Barra del Tordo y Tepehuaje, con flota de pangas y barcos menores. La acuacultura de camarón en granjas ha crecido, con 200 hectáreas de estanques.

**Agricultura**
Cultivos de sorgo, maíz, soya, y en menor medida caña de azúcar y cítricos. Superficie sembrada: 18,000 hectáreas. En la sierra, hay cultivos de café de altura, orgánico y de especialidad.

**Ganadería**
