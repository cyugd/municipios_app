- **Hortalizas:** tomate, chile, cebolla (300 hectáreas bajo riego). La tecnificación con riego por goteo ha incrementado la productividad.
- **Frutales:** mango, aguacate, papaya en pequeñas parcelas.

**Ganadería**
Ganado bovino (15,000 cabezas) para carne y leche. También porcino (3,000) y ovino (1,500). Existen praderas mejoradas de pasto buffel y estrella. La producción lechera abastece a la industria local de quesos y crema.

**Minería**
