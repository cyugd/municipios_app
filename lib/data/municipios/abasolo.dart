**Gastronomía**
Platillos típicos:
- **Cabrito al pastor:** Preparado con leña, adobado con especias, se sirve con tortillas de harina.
- **Machacado con huevo:** Carne seca deshebrada revuelta con huevo, servido con frijoles y tortillas.
- **Gorditas de horno:** De maíz quebrado, rellenas de frijol o queso, cocidas en horno de leña.
- **Asado de puerco:** En salsa roja, típico en celebraciones como bodas y bautizos.
- **Dulces de leche:** Como jamoncillos, cajetas y natillas.
- **Bebidas:** Agua de naranja, pulque de la región, y café de olla.

